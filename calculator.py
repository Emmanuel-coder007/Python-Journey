def add(a, b):
  value = a + b
  return value

def subtract(a, b):
  value = a - b
  return value

def multiply(a, b):
  value = a * b
  return value

def divide(a, b):
  value = a / b
  return value

def exp(a, b):
  value = a**b
  return value

